    public void AutoID()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/bank","root","");
            
            java.sql.Statement s= con.createStatement();
            ResultSet rs= s.executeQuery("select Max(cust_id) from customer");
            rs.next();
            rs.getString("Max(cust_id)");
            
            if(rs.getString("Max(cust_id)")==null)
            {
                txtcno.setText("CS001");
            }
            else
            {
                long id =Long.parseLong(rs.getString("Max(cust_id)").substring(2,rs.getString("Max(cust_id)").length()));
                id++;
                
                txtcno.setText("CS" + String.format("%03d",id));
                
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(cus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(cus.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void branch()
    {
         try {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/bank","root","");
            
            pst=con.prepareStatement("select * from branch");
            ResultSet rs= pst.executeQuery();
            txtbranch.removeAllItems();
            while(rs.next())
            {
                txtbranch.addItem(rs.getString(2));
            }
         } catch (SQLException ex) {
            Logger.getLogger(cus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(cus.class.getName()).log(Level.SEVERE, null, ex);
        }
    }




