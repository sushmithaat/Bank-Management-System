    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        // TODO add your handling code here:String cno=txtcno.getText();
            
            String cno=txtcno.getText();   
            String fn=txtfname.getText();
            String ln=txtlname.getText();
            String st=txtstreet.getText(); 
            String ct=txtcity.getText();
            String br=txtbranch.getSelectedItem().toString();
            String ph=txtphone.getText();
        if(fn.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter First Name!!");
        }
        else if(ln.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter Last Name!!");
        }
        else if(st.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter Street!!");
        }
        else if(ct.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter City!!");
        }
        else if(ph.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter Phone No!!");
        }
        else
        {
            try
            {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/bank","root","");
            
            pst = con.prepareStatement("insert into customer(cust_id,fname,lname,street,city,branch,phoneno)values(?,?,?,?,?,?,?)");
            pst.setString(1, cno);
            pst.setString(2, fn);
            pst.setString(3, ln);
            pst.setString(4, st);
            pst.setString(5, ct);
            pst.setString(6, br);
            pst.setString(7, ph);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Record Added!!!!");
            
            txtfname.setText("");
            txtlname.setText("");
            txtstreet.setText("");
            txtcity.setText("");
            txtbranch.setSelectedIndex(-1);
            txtphone.setText("");
            AutoID();
            txtfname.requestFocus();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(cus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(cus.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
          


    }//GEN-LAST:event_jButton3ActionPerformed

